function ColorBox() {
    const [color, setColor] = useState("gray");

    const changeColor = () => {
        setColor(arrayOfColors[Math.floor(Math.random() * arrayOfColors.length)]);
    };
  return (
    <div
      onClick={changeColor}
      style={{
        width: "300px",
        height: "300px",
        background: color,
        margin: "5px 5px"
      }}
    ></div>
  );
}

const arrOfComponent = [];

for (let i = 0; i < 30; i++) arrOfComponent.push(<ColorBox />);

ReactDOM.render(
  <div
    style={{
      display: "flex",
      flexWrap: "wrap"
    }}
  >
    {arrOfComponent}
  </div>,
  document.getElementById("root")
);
